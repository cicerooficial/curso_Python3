"""
Argumentos sep e end
sep e end são argumentos da função print que representam respectivamente "separador" e "fim".
"""
#Argumento nomeado sep
print('Avenida', 'Paulista', sep='-')

#Argumento nomeado end
print('Avenida', 'Paulista', end='###\n')

#Testando argumento separador para formatar um número de CPF aleatório
print('123','456','789',sep='.',end='-'+'00')